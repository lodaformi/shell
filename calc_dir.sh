#!/bin/bash

#统计某个文件夹下有多少个文件夹，包括子文件夹和隐藏文件夹
#注意不要把查找目录本身.和..算进来

#$1代表位置参数，接收执行程序的第一个参数
num=$(find $1 -type d |tail -n +2 | wc -l)
echo "Thu number of directory: $num"

echo "\$0 is $0"  #$0表示程序本身
echo "\$# is $#"  #$#表示参数的总个数


  #细节：
   #     find查找可以找出隐藏的文件夹
    #    不要把查找目录本身算进来，使用tail -n +2从第二行开始统计，将查找目录本身去掉
