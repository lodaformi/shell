#!/bin/bash
for ip in 10.107.13.{1..50}
do
        {
        ping -c 2 -i 0.2 -w 1 $ip &> /dev/null
        if (($? == 0))
        then
                echo "$ip is ok"
        fi
        } & 
        #放到后台执行可以并行计算，使用()或{}都行
done
wait #等待后台子进程执行完毕，传回最后一个命令的执行状态

#放到后台并行执行能加快很多速度，具体看机器的配置，比如这里我不放到后台需要46秒，放到后台只需要1秒
